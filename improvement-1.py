data = input("Please enter a number between 1 and 10:  ")   # Prompting user for input
if data >= 10:      # Creating a condition using an if-statement for the user's number to be up to or equal to 10.
    print("Yes you have successfully entered a number!")  # Printing a relevant statement to user.
else:
    print("You're number is out of range")   # If user does not enter a number up to or equal to 10, they will be
                                             # provided with a relevant print statement.

